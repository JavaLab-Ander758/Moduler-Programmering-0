
public class Oppgave1D {

	public static void main(String[] args) {
		
		char selection = 'C';
		
		switch (selection) {
		case 'A':
		case 'D':
			System.out.println("Du valgte A eller D");
			break;
		case 'B':
			System.out.println("Du valge B");
			break;
		case 'C':
			System.out.println("Du valgte C");
		}
	}

}
